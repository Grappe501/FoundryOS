export function ensureMarketingScaffold(written: string[], skipped: string[]): void {
  const dirs = [
    'channels/seo',
    'channels/youtube',
    'channels/tiktok',
    'channels/facebook',
    'channels/homeschool',
    'channels/parents',
    'channels/educators',
    'lead-magnets',
    'email-sequences',
    'launch-calendars',
    'affiliates',
    'partnerships',
    'influencers',
    'milestones',
    'worlds',
  ];
  for (const d of dirs) {
    const readme = `# ${d.split('/').pop()}

Channel scaffold — PASS-031 Marketing Factory.

World-specific assets live in \`marketing/worlds/{slug}/\`.
`;
    writeFileTracked(path.join(marketingRoot(), d, 'README.md'), readme, written, skipped);
  }
}

export function manufactureMarketingPack(
  bp: MarketingBlueprint,
  opts: { force?: boolean } = {},
): { written: string[]; skipped: string[]; manifest: MarketingManifest } {
  const written: string[] = [];
  const skipped: string[] = [];
  const dir = worldMarketingDir(bp.slug);
  const write = opts.force ? writeFileForce : writeFileTracked;

  const files: Record<string, string> = {
    'README.md': `# ${bp.display_name} — Marketing World Pack

> **PASS-031 Marketing Factory** · \`npm run build:marketing -- ${bp.slug}\`

| Field | Value |
|-------|-------|
| Frame | ${bp.frame} |
| Outcome | ${bp.outcome_statement} |
| Priority | ${bp.priority_tier} |
| Consumer | \`/${bp.slug}\` |

## Artifacts

${MARKETING_ARTIFACTS.map((a) => `- ${a}`).join('\n')}

80% marketing effort → ai-builder, financial-independence, public-speaking.
`,
    'seo-map.md': `# ${bp.display_name} — SEO Map\n\nSee \`seo-top-100.md\` and \`seo-cluster-map.json\`.`,
    'seo-top-100.md': seoTop100(bp),
    'seo-cluster-map.json': seoClusterJson(bp),
    'youtube-topics.md': youtubeTopics(bp),
    'tiktok-topics.md': tiktokTopics(bp),
    'lead-magnets.md': leadMagnets(bp),
    'email-sequence.md': emailSequence(bp),
    'parent-messaging.md': parentMessaging(bp),
    'educator-messaging.md': educatorMessaging(bp),
    'social-calendar.md': socialCalendar(bp),
    'content-calendar-30d.md': contentCalendar30(bp),
    'partnership-targets.md': partnershipTargets(bp),
    'LAUNCH_CHECKLIST.md': launchChecklist(bp),
    'growth-plan.json': growthPlanJson(bp),
  };

  for (const [name, content] of Object.entries(files)) {
    write(path.join(dir, name), content, written, skipped);
  }

  const manifest: MarketingManifest = {
    slug: bp.slug,
    display_name: bp.display_name,
    generated_at: new Date().toISOString(),
    pass: 'PASS-031',
    priority_tier: bp.priority_tier,
    artifacts: [...MARKETING_ARTIFACTS],
    automation_pct: 95,
  };

  const manifestPath = path.join(dir, 'manifest.json');
  if (opts.force) {
    writeFileForce(manifestPath, JSON.stringify(manifest, null, 2), written);
  } else {
    writeFileTracked(manifestPath, JSON.stringify(manifest, null, 2), written, skipped);
  }

  return { written, skipped, manifest };
}

