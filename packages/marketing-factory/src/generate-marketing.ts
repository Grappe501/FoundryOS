function seoTop100(bp: MarketingBlueprint): string {
  const base = [
    `What Is ${bp.display_name}?`,
    `Beginner Guide to ${bp.display_name}`,
    `Road to ${bp.outcome_statement}`,
    `${bp.display_name} for Students`,
    `${bp.display_name} for Parents`,
    `${bp.display_name} for Educators`,
    `${bp.display_name} Projects`,
    `Common ${bp.display_name} Mistakes`,
    `Best ${bp.display_name} Resources`,
    `${bp.display_name} Portfolio Examples`,
    ...bp.seo_pages.map((p) => p.title),
    ...bp.missions.map((m) => `${m.title} — Step by Step`),
    ...bp.projects.map((p) => `How to ${p}`),
    ...bp.projects.map((p) => `${p} Checklist`),
    `${bp.community_name} — Join the Community`,
    `${bp.tier_2_hook.split('—')[0]?.trim()} Guide`,
    `Is ${bp.display_name} Worth Learning?`,
    `${bp.display_name} vs Courses`,
    `${bp.display_name} for Career Changers`,
    `${bp.display_name} for Homeschool`,
  ];

  const modifiers = [
    'for beginners',
    'for teens',
    'for adults',
    'without experience',
    'in 30 days',
    'step by step',
    'free guide',
    'checklist',
    'worksheet',
    'template',
  ];

  const rows: string[] = [];
  for (const topic of base) {
    rows.push(`| ${rows.length + 1} | ${topic} | informational | draft |`);
    if (rows.length >= 100) break;
  }
  for (const topic of base.slice(0, 15)) {
    for (const mod of modifiers) {
      if (rows.length >= 100) break;
      rows.push(`| ${rows.length + 1} | ${topic} ${mod} | long-tail | draft |`);
    }
  }

  return `# ${bp.display_name} — Top 100 SEO Targets

> Generated by \`npm run build:marketing -- ${bp.slug}\` (PASS-031)

Publish when content_score >= 70.

| # | Target | Intent | Status |
|---|--------|--------|--------|
${rows.slice(0, 100).join('\n')}
`;
}

function seoClusterJson(bp: MarketingBlueprint): string {
  const pages = bp.seo_pages.map((p, i) => ({
    slug: p.slug,
    title: p.title,
    intent: p.intent,
    priority: i + 1,
    status: 'draft',
  }));
  return JSON.stringify(
    {
      domain: bp.slug,
      display_name: bp.display_name,
      cluster_size: pages.length,
      pages,
      publish_gate: { min_content_score: 70 },
    },
    null,
    2,
  );
}

function youtubeTopics(bp: MarketingBlueprint): string {
  const missionTopics = bp.missions.map((m) => `- Mission ${m.title}: full walkthrough`);
  const projectTopics = bp.projects.map((p) => `- Build: ${p} (screen + voiceover)`);
  const outcomeTopics = [
    `- ${bp.outcome_statement} — 12-week journey overview`,
    `- Parent guide: ${bp.parent_headline}`,
    `- Why ${bp.frame} matters in 2026`,
    `- ${bp.community_name} tour`,
    `- Portfolio review: what good looks like`,
  ];
  const filler = [
    `- Day in the life of a ${bp.display_name} learner`,
    `- Common mistakes (and how Foundry fixes them)`,
    `- Free vs Build vs Mastery — honest comparison`,
    `- Educator classroom setup for ${bp.display_name}`,
    `- Student showcase reaction`,
  ];
  const all = [...missionTopics, ...projectTopics, ...outcomeTopics, ...filler];
  while (all.length < 50) {
    all.push(`- FAQ: ${bp.display_name} question ${all.length + 1}`);
  }
  return `# ${bp.display_name} — Top 50 YouTube Topics

${all.slice(0, 50).join('\n')}
`;
}

