function appendCorePromise(bp: FullWorldBlueprint, written: string[], skipped: string[]): boolean {
  const file = path.join(platformRoot(), `lib/${worldLibBasename(bp.slug)}.ts`);
  if (!fs.existsSync(file)) return false;
  const c = slugToConst(bp.slug);
  const marker = `${c}_CORE_PROMISE`;
  const existing = fs.readFileSync(file, 'utf8');
  if (existing.includes(marker)) return false;

  const append = `\nexport const ${marker} = '${bp.corePromise.replace(/'/g, "\\'")}';\n`;
  fs.writeFileSync(file, existing.replace(/\s*$/, '') + append, 'utf8');
  written.push(file);
  return true;
}

