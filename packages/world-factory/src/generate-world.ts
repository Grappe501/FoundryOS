export function regenerateFactoryRegistry(slugs: string[]): void {
  const blueprints = slugs
    .map((s) => getFullBlueprint(s))
    .filter((b): b is FullWorldBlueprint => Boolean(b));

  const routes = blueprints.map((b) => `  '/${b.slug}',`).join('\n');
  const routesContent = `/** Auto-generated by @foundry/world-factory — do not edit */

export const FACTORY_CONSUMER_ROUTES = [
${routes}
] as const;

export type FactoryConsumerRoute = (typeof FACTORY_CONSUMER_ROUTES)[number];
`;
  writeFileIfChanged(ROUTES_PATH(), routesContent);

  const exploreEntries = blueprints
    .map((b) => {
      const categories = b.exploreCategories.map((c) => `'${c}'`).join(', ');
      return `  {
    slug: '${b.slug}',
    name: '${b.displayName}',
    outcome: '${b.outcomeStatement}',
    status: 'live' as const,
    tier: '${b.exploreTier}' as const,
    launch_rank: ${b.exploreLaunchRank},
    section_id: '${b.exploreSectionId}',
    categories: [${categories}] as const,
    live_href: '/${b.slug}',
    planned_projects: ${JSON.stringify(b.plannedProjects)},
    become: '${b.exploreBecome.replace(/'/g, "\\'")}',
  },`;
    })
    .join('\n');

  const exploreContent = `/** Auto-generated by @foundry/world-factory — do not edit */
import type { ExplorePath } from '../explore-catalog';

export const FACTORY_EXPLORE_PATHS: ExplorePath[] = [
${exploreEntries}
];
`;
  writeFileIfChanged(EXPLORE_PATH(), exploreContent);
}

export function generateWorld(slug: string): GenerateWorldResult {
  const bp = getFullBlueprint(slug);
  if (!bp) {
    throw new Error(`Unknown world slug: ${slug}`);
  }

  const written: string[] = [];
  const skipped: string[] = [];

  const worldLibCreated = bp.slug === 'civic-engagement' ? generateCivicWorldLib(bp, written, skipped) : false;
  const corePromiseAppended = appendCorePromise(bp, written, skipped);

  generateWorldMeta(bp, written, skipped);
  generateLayout(bp, written, skipped);
  generateHubPage(bp, written, skipped);
  generateSubPages(bp, written, skipped);
  generateComponents(bp, written, skipped);
  generateVerticalPage(bp, written, skipped);
  generateMarketingPack(bp, written, skipped);

  const manifest = loadManifest();
  const slugs = [...new Set([...manifest.domains, slug])];
  saveManifestSlugs(slugs);
  regenerateFactoryRegistry(slugs);

  return {
    slug,
    displayName: bp.displayName,
    filesWritten: written.map((f) => path.relative(getRepoRoot(), f)),
    filesSkipped: skipped.map((f) => path.relative(getRepoRoot(), f)),
    corePromiseAppended,
    worldLibCreated,
  };
}
