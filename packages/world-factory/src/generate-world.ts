const MANIFEST_PATH = () =>
  path.join(platformRoot(), 'lib/generated/world-factory-manifest.json');
const ROUTES_PATH = () =>
  path.join(platformRoot(), 'lib/generated/world-factory-routes.ts');
const EXPLORE_PATH = () =>
  path.join(platformRoot(), 'lib/generated/world-factory-explore.ts');

export function loadManifest(): WorldFactoryManifest {
  const file = MANIFEST_PATH();
  if (!fs.existsSync(file)) {
    return { generated_at: new Date().toISOString(), domains: [] };
  }
  return JSON.parse(fs.readFileSync(file, 'utf8')) as WorldFactoryManifest;
}

export function saveManifestSlugs(slugs: string[]): void {
  const unique = [...new Set(slugs)].sort();
  const manifest: WorldFactoryManifest = {
    generated_at: new Date().toISOString(),
    domains: unique,
  };
  ensureDir(path.dirname(MANIFEST_PATH()));
  fs.writeFileSync(MANIFEST_PATH(), `${JSON.stringify(manifest, null, 2)}\n`, 'utf8');
}

function track(
  filePath: string,
  content: string,
  written: string[],
  skipped: string[],
): void {
  if (writeFileIfChanged(filePath, content)) written.push(filePath);
  else skipped.push(filePath);
}

function relImportDepth(fromDir: string, toFile: string): string {
  const rel = path.relative(fromDir, toFile).replace(/\\/g, '/');
  return rel.startsWith('.') ? rel : `./${rel}`;
}

function libImport(fromAppFile: string, slug: string): string {
  const lib = path.join(platformRoot(), `lib/${worldLibBasename(slug)}.ts`);
  const noExt = relImportDepth(path.dirname(fromAppFile), lib).replace(/\.ts$/, '');
  return noExt;
}

function metaImport(fromAppFile: string, slug: string): string {
  const meta = path.join(platformRoot(), `lib/${worldLibBasename(slug)}-meta.ts`);
  const noExt = relImportDepth(path.dirname(fromAppFile), meta).replace(/\.ts$/, '');
  return noExt;
}

function componentImport(fromAppFile: string, slug: string, component: string): string {
  const comp = path.join(platformRoot(), `components/${componentFolder(slug)}/${component}.tsx`);
  const noExt = relImportDepth(path.dirname(fromAppFile), comp).replace(/\.tsx$/, '');
  return noExt;
}

